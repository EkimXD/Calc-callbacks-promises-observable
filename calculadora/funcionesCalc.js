module.exports.sumar = (numero1, numero2) => Number(numero1) + Number(numero2);
module.exports.restar = (numero1, numero2) => Number(numero1) - Number(numero2);
module.exports.multiplicar = (numero1, numero2) => Number(numero1) * Number(numero2);
module.exports.dividir = (numero1, numero2) => Number(numero1) / Number(numero2);